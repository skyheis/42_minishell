#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	k;

	k = 0;
	if (s)
	{
		while (s[k] != '\0')
		{
			write(fd, &s[k], 1);
			k++;
		}
	}
	return (k);
}
