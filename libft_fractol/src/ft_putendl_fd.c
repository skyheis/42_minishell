#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	unsigned int	k;

	k = 0;
	if (s)
	{
		while (s[k] != '\0')
		{
			write(fd, &s[k], 1);
			k++;
		}
		write(fd, "\n", 1);
	}
}
